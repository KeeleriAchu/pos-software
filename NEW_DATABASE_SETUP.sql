-- ── 1. CUSTOMERS ──────────────────────────────────────────
CREATE TABLE IF NOT EXISTS customers (
  id           UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  name         TEXT,                        -- Optional: not required for walk-in
  phone        TEXT UNIQUE,                 -- Optional: required only for pending customers
  email        TEXT,
  address      TEXT,
  notes        TEXT,                        -- Free notes about the customer
  total_visits INT DEFAULT 0,               -- Auto-incremented on each bill
  created_at   TIMESTAMP DEFAULT NOW(),
  updated_at   TIMESTAMP DEFAULT NOW()
);

-- ── 2. PRODUCTS ───────────────────────────────────────────
CREATE TABLE IF NOT EXISTS products (
  id           UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  name         TEXT NOT NULL,
  price        DECIMAL(10,2) NOT NULL,
  cost_price   DECIMAL(10,2) DEFAULT 0,     -- Purchase cost for profit calculation
  stock        INT DEFAULT 0,               -- Current stock count
  unit         TEXT DEFAULT 'pcs',          -- Unit label: kg, litre, pcs, box...
  low_stock_threshold INT DEFAULT 10,       -- Alert when stock <= this value
  category     TEXT,
  barcode      TEXT,
  is_active    BOOLEAN DEFAULT TRUE,
  created_at   TIMESTAMP DEFAULT NOW(),
  updated_at   TIMESTAMP DEFAULT NOW()
);

-- ── 3. BILLS ──────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS bills (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  bill_number     TEXT NOT NULL UNIQUE,    -- Auto-generated by trigger
  customer_id     UUID REFERENCES customers(id) ON DELETE SET NULL,
  total_amount    DECIMAL(10,2) NOT NULL,
  paid_amount     DECIMAL(10,2) DEFAULT 0,
  pending_amount  DECIMAL(10,2) DEFAULT 0,
  discount_amount DECIMAL(10,2) DEFAULT 0,
  payment_status  TEXT DEFAULT 'pending', -- 'paid', 'pending', 'partial'
  payment_method  TEXT DEFAULT 'cash',   -- 'cash', 'upi', 'card'
  notes           TEXT,
  created_at      TIMESTAMP DEFAULT NOW(),
  updated_at      TIMESTAMP DEFAULT NOW()
);

-- ── 4. BILL ITEMS ─────────────────────────────────────────
CREATE TABLE IF NOT EXISTS bill_items (
  id           UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  bill_id      UUID REFERENCES bills(id) ON DELETE CASCADE,
  product_id   UUID REFERENCES products(id) ON DELETE SET NULL,
  product_name TEXT NOT NULL,              -- Snapshot at time of sale
  quantity     INT NOT NULL,
  unit_price   DECIMAL(10,2) NOT NULL,
  cost_price   DECIMAL(10,2) DEFAULT 0,   -- Snapshot for profit calculation
  total_price  DECIMAL(10,2) NOT NULL,
  created_at   TIMESTAMP DEFAULT NOW()
);

-- ── 5. PENDING PAYMENTS ───────────────────────────────────
CREATE TABLE IF NOT EXISTS pending_payments (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  customer_id     UUID REFERENCES customers(id) ON DELETE CASCADE,
  bill_id         UUID REFERENCES bills(id) ON DELETE SET NULL,
  amount_due      DECIMAL(10,2) NOT NULL,
  amount_paid     DECIMAL(10,2) DEFAULT 0,
  due_date        DATE,
  reminder_sent   BOOLEAN DEFAULT FALSE,
  last_reminder_at TIMESTAMP,
  status          TEXT DEFAULT 'unpaid',  -- 'unpaid', 'partial', 'paid'
  notes           TEXT,
  created_at      TIMESTAMP DEFAULT NOW(),
  updated_at      TIMESTAMP DEFAULT NOW()
);

-- ── 6. PAYMENT HISTORY ────────────────────────────────────
CREATE TABLE IF NOT EXISTS payment_history (
  id             UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  customer_id    UUID REFERENCES customers(id) ON DELETE SET NULL,
  bill_id        UUID REFERENCES bills(id) ON DELETE SET NULL,
  pending_id     UUID REFERENCES pending_payments(id) ON DELETE SET NULL,
  amount         DECIMAL(10,2) NOT NULL,
  payment_method TEXT DEFAULT 'cash',
  note           TEXT,
  paid_at        TIMESTAMP DEFAULT NOW()
);

-- ── 7. CUSTOMER VISIT HISTORY ─────────────────────────────
-- Tracks every time a customer makes a purchase
CREATE TABLE IF NOT EXISTS customer_visits (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  customer_id UUID REFERENCES customers(id) ON DELETE CASCADE,
  bill_id     UUID REFERENCES bills(id) ON DELETE SET NULL,
  amount      DECIMAL(10,2) NOT NULL,
  visited_at  TIMESTAMP DEFAULT NOW()
);

-- ── 8. REMINDERS LOG ──────────────────────────────────────
CREATE TABLE IF NOT EXISTS reminders_log (
  id                 UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  customer_id        UUID REFERENCES customers(id) ON DELETE SET NULL,
  pending_payment_id UUID REFERENCES pending_payments(id) ON DELETE SET NULL,
  type               TEXT NOT NULL,  -- 'whatsapp', 'sms', 'notification'
  message            TEXT,
  status             TEXT DEFAULT 'sent',  -- 'sent', 'failed'
  sent_at            TIMESTAMP DEFAULT NOW()
);

-- ── 9. DAILY SUMMARY (cache for fast dashboard) ───────────
-- Auto-populated by triggers each day
CREATE TABLE IF NOT EXISTS daily_summary (
  id            UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  summary_date  DATE NOT NULL UNIQUE,
  total_sales   DECIMAL(10,2) DEFAULT 0,
  total_profit  DECIMAL(10,2) DEFAULT 0,
  total_bills   INT DEFAULT 0,
  customers_visited INT DEFAULT 0,
  created_at    TIMESTAMP DEFAULT NOW(),
  updated_at    TIMESTAMP DEFAULT NOW()
);

